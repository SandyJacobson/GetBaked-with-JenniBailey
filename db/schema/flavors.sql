/* Table: flavors                                               */
/*==============================================================*/
create table flavors (
   objid                SERIAL               not null,
   flavor_name          VARCHAR(30)          null,
   constraint PK_FLAVORS primary key (objid)
);

/*==============================================================*/
/* Index: flavors_pk                                            */
/*==============================================================*/
create unique index flavors_pk on flavors (
objid
);

